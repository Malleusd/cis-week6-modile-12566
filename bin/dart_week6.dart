void main(){
  int x = 30;
  double y = 20;

  print("The example of Relational");
  bool res1 = x > y;
  print("x>y :$x > $y = $res1");

  bool res2 = x < y;
  print("x<y: $x < $y = $res2");

  bool res3 = x >= y;
  print("x>=y: $x <= $y = $res3");

  bool res4 = x <= y;
  print("x<=y: $x <= $y = $res4");

  bool res5 = x != y;
  print("x!=y: $x != $y += $res5");

  bool res6 = x == y;
  print("x==y: $x == $y = $res6");
}